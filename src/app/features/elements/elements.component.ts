    selectElement(element: Element) {
        this.selectedElement = element;
        // Scroll to details section
        setTimeout(() => {
            document.getElementById('element-details')?.scrollIntoView({ behavior: 'smooth' });
        }, 100);
    }

    closeDetails() {
        this.selectedElement = null;
    }

    getElementIconClass(elementId: string): string {
        switch (elementId) {
            case 'fire': return 'fas fa-fire';
            case 'earth': return 'fas fa-mountain';
            case 'air': return 'fas fa-wind';
            case 'water': return 'fas fa-water';
            default: return 'fas fa-star';
        }
    }

    getCompatibilityColor(type: string): string {
        switch (type) {
            case 'best':
                return '#4ade80';
            case 'good':
                return '#fbbf24';
            case 'challenging':
                return '#fb923c';
            default:
                return '#94a3b8';
        }
    }
